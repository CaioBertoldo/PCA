#include <stdio.h>

int main()
{
    // Variáveis
    int n1, n2, soma;

    // Entrada
    printf("Digite um numero: ");
    scanf("%d", &n1);
    printf("\n Digite outro numero: ");
    scanf("%d",&n2);

    // Processamento
    soma = n1 + n2;

    // Saída
    printf("\n A soma entre %d e %d e igual a %d\n",n1,n2,soma);
    return 0;
}
